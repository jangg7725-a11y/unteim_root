/**
 * shinsalHighlights 칩 문자열에서 신살 이름과 분류를 파싱합니다.
 * 예) "귀인: 천을귀인" → { name: "천을귀인", type: "good" }
 *     "주의: 반안살"   → { name: "반안살",   type: "caution" }
 *     "신살: 역마살"   → { name: "역마살",   type: "neutral" }
 */
export function parseShinsalChip(chip: string): {
  name: string;
  type: "good" | "caution" | "neutral";
} {
  const s = chip.trim();
  if (s.startsWith("귀인:")) {
    return { name: s.replace("귀인:", "").trim(), type: "good" };
  }
  if (s.startsWith("주의:")) {
    return { name: s.replace("주의:", "").trim(), type: "caution" };
  }
  return { name: s.replace(/^신살:\s*/, "").trim(), type: "neutral" };
}
