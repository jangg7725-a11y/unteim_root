/**
 * 감정 코칭 텍스트를 딱딱한 서술체(습니다·됩니다·하세요)에서
 * 공감하는 대화형 상담가 어투로 변환한다.
 *
 * 목표: 사용자가 "알아주는구나, 공감해주는구나"를 느낄 수 있도록.
 * 적용 순서: 구체적·긴 패턴 → 일반 어미 순 (오버라이드 방지).
 */

const PATTERNS: [RegExp, string][] = [
  // ── 공감 어미: 구체적 어구 먼저 ─────────────────────────────────────
  [/느껴질 수 있습니다/g,     "느끼시는군요~"],
  [/느껴지실 수 있습니다/g,   "느끼시는군요~"],
  [/내려앉을 수 있습니다/g,   "내려앉으실 수도 있겠어요~"],
  [/무거워질 수 있습니다/g,   "무거워지실 수 있겠어요~"],
  [/무거워집니다/g,           "무거워지기도 해요~"],

  // ── 권유·조언 어미 ─────────────────────────────────────────────────
  [/하는 것이 좋습니다/g,     "해보시는 건 어떨까요~"],
  [/는 것이 좋습니다/g,       "는 게 좋을 것 같아요~"],
  [/것이 좋습니다/g,          "게 좋을 것 같아요~"],
  [/편이 안전합니다/g,        "편이 마음 편할 거예요~"],
  [/편이 좋습니다/g,          "편이 좋을 것 같아요~"],

  // ── 필요·중요 ──────────────────────────────────────────────────────
  [/필요합니다/g,             "필요해요~"],

  // ── 경고·주의 ──────────────────────────────────────────────────────
  [/주의하세요/g,             "조심해주세요~"],

  // ── 하세요 계열 (구체적 → 일반) ───────────────────────────────────
  [/적어두세요/g,             "적어두시면 좋아요~"],
  [/잡아두세요/g,             "잡아두시면 좋아요~"],
  [/해두세요/g,               "해두시면 좋아요~"],
  [/잡으세요/g,               "잡아보세요~"],
  [/하세요/g,                 "해보세요~"],

  // ── 수 있습니다 ───────────────────────────────────────────────────
  [/수 있습니다/g,            "수 있겠어요~"],

  // ── 특별 공감 어미 ─────────────────────────────────────────────────
  [/회복됩니다/g,             "회복되구요^^"],
  [/커집니다/g,               "커질 수 있어요~"],
  [/이어집니다/g,             "이어지기도 해요~"],

  // ── 됩니다 계열 (위 특수 케이스 처리 후) ──────────────────────────
  [/됩니다/g,                 "돼요~"],

  // ── 겠습니다 ──────────────────────────────────────────────────────
  [/겠습니다/g,               "겠어요~"],

  // ── 있습니다 ──────────────────────────────────────────────────────
  [/있습니다/g,               "있어요~"],

  // ── 합니다 → 해요 ─────────────────────────────────────────────────
  [/합니다/g,                 "해요"],

  // ── 입니다 → 이에요 ───────────────────────────────────────────────
  [/입니다/g,                 "이에요"],

  // ── 집니다 (커지다·이어지다 등 잔여) ─────────────────────────────
  [/집니다/g,                 "져요~"],

  // ── "이 부분은" → 더 따뜻한 표현 ─────────────────────────────────
  [/이 부분은/g,              "이런 흐름은"],
];

/**
 * emotionCoaching 텍스트를 공감 대화형 어투로 변환한다.
 */
export function warmifyEmotionCoaching(raw: string): string {
  if (!raw) return raw;
  let t = raw;
  for (const [pat, rep] of PATTERNS) {
    t = t.replace(pat, rep);
  }
  // 연속 물결(~~) 정리
  t = t.replace(/~{2,}/g, "~");
  return t;
}
