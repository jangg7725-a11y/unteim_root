/* ─────────────────────────────────────────────────────────────────
   에너지 레벨 판별
────────────────────────────────────────────────────────────────── */
type EnergyLevel = "high" | "mid" | "low";

function getEnergyLevel(m: MonthlyFortuneEngineMonth): EnergyLevel {
  const ls = m.luckScore ?? (m.score * 20);
  const hasGuiin = (m.shinsalHighlights ?? []).some((h) => /^귀인:/.test(h));
  if (ls >= 62 || (hasGuiin && ls >= 50)) return "high";
  if (ls >= 40) return "mid";
  return "low";
}

