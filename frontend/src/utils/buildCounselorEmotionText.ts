/* ─────────────────────────────────────────────────────────────────
   5. 점수별 따뜻한 마무리 응원
────────────────────────────────────────────────────────────────── */
function closingWarm(luckScore?: number): string {
  const s = luckScore ?? 50;
  if (s >= 70)
    return "이달 전반적인 흐름은 나쁘지 않아요. 지금 방향 그대로 가세요~ 응원하고 있어요 😊";
  if (s >= 50)
    return "크게 무리하지 않으면 이달 잘 지나가실 거예요. 조금씩 꾸준히가 답이에요~ 잘 하고 계세요 😊";
  return "조금 무거운 달이지만, 이런 시간도 다음을 위한 단단한 준비가 되어요. 지금 이 시간도 충분히 의미 있어요~ 😊";
}

