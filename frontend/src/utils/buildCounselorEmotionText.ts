/* ─────────────────────────────────────────────────────────────────
   메인 export
────────────────────────────────────────────────────────────────── */
export function buildCounselorEmotionText(m: MonthlyFortuneEngineMonth): string[] {
  const stage = (m.twelveStage || "").trim();
  const sipsin = (m.stemTenGod || "").trim();
  const hints = m.interactionHints ?? [];
  const level = getEnergyLevel(m);

  const lines: string[] = [];

  // 1. 개막 공감 (에너지 × 12운성)
  const open = STAGE_OPEN[level][stage] ?? STAGE_OPEN.mid[stage] ?? "";
  if (open) lines.push(open);

  // 2. 운세 연결 (귀인 신살 or 에너지 방향)
  const fortune = getFortuneConnection(m, level);
  if (fortune) lines.push(fortune);

  // 3. 십성 감정 코칭 (에너지 × 십성)
  const sipsinLine = SIPSIN_EMOTION[level][sipsin] ?? SIPSIN_EMOTION.mid[sipsin] ?? "";
  if (sipsinLine) lines.push(sipsinLine);

  // 4. 합·충·형 상황 힌트 (있을 때만)
  const intNote = interactionNote(hints, level);
  if (intNote) lines.push(intNote);

  // 5. 행동 실천 코칭 (에너지 × 12운성) — 십성 코칭과 겹치지 않도록 마지막에
  const action = STAGE_ACTION[level][stage] ?? STAGE_ACTION.mid[stage] ?? "";
  if (action && action !== open) lines.push(action);

  // 6. 마무리 응원
  lines.push(closingWarm(level, m.luckScore));

  return lines;
}
