/**
 * 상담가 어투 감정 코칭 단락 배열을 반환한다.
 * 각 항목은 독립된 문단으로 렌더링한다.
 */
export function buildCounselorEmotionText(m: MonthlyFortuneEngineMonth): string[] {
  const stage = (m.twelveStage || "").trim();
  const sipsin = (m.stemTenGod || "").trim();
  const hints = m.interactionHints ?? [];

  const lines: string[] = [];

  // 1. 개막 공감
  const open = STAGE_OPEN[stage];
  if (open) lines.push(open);

  // 2. 십성 감정 이유 + 코칭
  const sipsinLine = SIPSIN_EMOTION[sipsin];
  if (sipsinLine) lines.push(sipsinLine);

  // 3. 합·충·형 상황 힌트 (있을 때만)
  const intNote = interactionNote(hints);
  if (intNote) lines.push(intNote);

  // 4. 행동 실천 코칭
  const action = STAGE_ACTION[stage];
  if (action) lines.push(action);

  // 5. 마무리 응원
  lines.push(closingWarm(m.luckScore));

  return lines;
}
