# ---------------------------------------------------------
# 2) 룰 처리 엔진
# ---------------------------------------------------------
def analyze_sipsin_profiles(ctx) -> List[Dict[str, Any]]:
    """
    조건(when) 식을 하나씩 평가하여
    해당되는 룰들을 모두 리턴한다.
    """
    results = []
    for rule in SIPSIN_RULES:
        try:
            if rule["when"](ctx):
                results.append({
                    "id": rule["id"],
                    "category": rule["category"],
                    "message": rule["message"]
                })
        except Exception:
            # 안전장치: 조건식 오류 발생해도 엔진 전체가 죽지 않도록
            continue

    return results


# ---------------------------------------------------------
# 3) 최종 메시지 합치기 (리포트용)
# ---------------------------------------------------------
def summarize_sipsin_messages(rules: List[Dict[str, Any]]) -> Dict[str, List[str]]:
    """
    카테고리별 메시지 묶음.
    예:
    {
        "성향": [...],
        "재물": [...],
        "직업": [...],
        ...
    }
    """
    result = {}
    for item in rules:
        cat = item["category"]
        if cat not in result:
            result[cat] = []
        result[cat].append(item["message"])
    return result
