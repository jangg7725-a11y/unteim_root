def build_day_master_commentary(day_gan: str, oheng_counts: Dict[str, int]) -> Dict[str, Any]:
    """
    반환 예:
      {
        "label": "무토",
        "base": "...",
        "strengths": [...],
        "weaknesses": [...],
        "modifiers": ["...", "..."]  # 오행 분포 기반 조건부 문장
      }
    """
    norm = normalize_day_gan(day_gan)
    gan = norm["gan"]

    prof = DAY_MASTER_PROFILES.get(gan)
    if not prof:
        return {
            "label": norm.get("label", day_gan),
            "base": "",
            "strengths": [],
            "weaknesses": [],
            "modifiers": [],
            "warning": f"day_gan profile not found: {day_gan}",
        }

    # 조건부 문장: 특정 오행이 과다/부족할 때만 붙이기 (기본 룰)
    modifiers: List[str] = []
    counts = oheng_counts or {}
    for e in ELEMENTS:
        c = int(counts.get(e, 0) or 0)
        # 과다(>=3)면 문장 추가, 부족(==0)면 반대 문장도 가능(추후 확장)
        if c >= 3 and e in prof.by_oheng:
            modifiers.append(prof.by_oheng[e])

    return {
        "label": prof.label,
        "base": prof.base,
        "strengths": prof.strengths,
        "weaknesses": prof.weaknesses,
        "modifiers": modifiers,
    }
