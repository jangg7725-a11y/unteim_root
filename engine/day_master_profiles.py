# --- 1) 천간/오행 기본 매핑 ---
GAN_TO_KOR = {
    "甲": "갑", "乙": "을", "丙": "병", "丁": "정", "戊": "무",
    "己": "기", "庚": "경", "辛": "신", "壬": "임", "癸": "계",
}

GAN_TO_ELEMENT = {
    "甲": "목", "乙": "목",
    "丙": "화", "丁": "화",
    "戊": "토", "己": "토",
    "庚": "금", "辛": "금",
    "壬": "수", "癸": "수",
}

ELEMENTS = ["목", "화", "토", "금", "수"]


def normalize_day_gan(day_gan: str) -> Dict[str, str]:
    """
    입력 예:
      - "戊" 또는 "무" 또는 "무토" 같은 값이 들어올 수 있어 방어적으로 정규화.
    반환:
      {"gan": "戊", "kor": "무", "elem": "토", "label": "무토"}
    """
    s = (day_gan or "").strip()

    # 1) "戊" 같은 한자 천간이면 그대로
    if s in GAN_TO_KOR:
        gan = s
    else:
        # 2) "무" 같은 한글이면 역매핑
        rev = {v: k for k, v in GAN_TO_KOR.items()}
        if s in rev:
            gan = rev[s]
        else:
            # 3) "무토" 같은 형태면 앞 1글자(무/기/갑...) 우선
            if len(s) >= 1 and s[0] in rev:
                gan = rev[s[0]]
            else:
                gan = s  # 알 수 없으면 그대로

    kor = GAN_TO_KOR.get(gan, "")
    elem = GAN_TO_ELEMENT.get(gan, "")
    label = f"{kor}{elem}" if kor and elem else (s or gan)
    return {"gan": gan, "kor": kor, "elem": elem, "label": label}


